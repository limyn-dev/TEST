public class CEtudiantR extends CEtudiant
  {

  public CEtudiantR()
    { super(); setNote(0); }
  public CEtudiantR(String nom, int anDebut, String section, int note )
    { super(nom, section, section); setNote(note); }

  public void setNote(int note)   { Nte = note ; }

  public int getNote()            { return Nte ; }

  //---------------------------------------------------------------------------
  private int nte ;
}